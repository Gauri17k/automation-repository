package vaiablesAndDataTypes;

public class DataTypes {

	public static void main(String[] args) {
		
		//integer
		int age=30;
		System.out.println("age is "+ age);
		
		//characher
		
		char gender ='F';
		
		System.out.println("geneder is "+ gender);
		//Decimal values- float or double
		
		double marks=7.5;
		float s=5.1f;
		
		System.out.println("marks is " + marks);
		System.out.println("float value "+ s);
		
	   //Boolean value
		boolean status= true;
		System.out.println("status  is "+ status);
		
		// strings
		
		String name="Gauri";
		System.out.println("name is "+ name);
		
		// for integer values
		byte a=10;
		short b=20;
		int c= 30;
		long d=40;
		
		
	}

}
